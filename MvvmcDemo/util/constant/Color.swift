import UIKit


extension UIColor {

    // orange
    public static var primary: UIColor {
        return UIColor(hex: 0xFD9549)!
    }

    // light teal
    public static var secondary: UIColor {
        return UIColor(hex: 0x25CAD3)!
    }

    // light orange
    public static var primaryLight: UIColor {
        return UIColor(hex: 0xFDBB7E)!
    }
}
